package operator;

public class Operator1 {
    public static void main(String[] args) {
        // 변수 초기화
        int a = 5;
        int b = 2;

        // 더하기
        int sum = a + b;
        System.out.println("a + b = " + sum);

        // 빼기
        int diff = a - b;
        System.out.println("a - b = " + diff);

        // 곱하기
        int multi = a * b;
        System.out.println("a * b = " + multi);

        // 나누기
        int div = a / b;
        System.out.println("a / b = " + div);

        // 나머지
        int mod = a % b;
        System.out.println("a % b = " + mod);
    }
}
